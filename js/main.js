	// response invite
	var InviteResponse = function () {
		$('.invite_response').on('click', function(event){
			
			event.preventDefault();
			loaderPage('show');

			$(".invite_response").removeClass("btn-success");
			$(".invite_response").removeClass("btn-warning");
			$(".invite_response").addClass("btn-default");

			if ($(this).data('agree') == '1') {
				$(this).removeClass("btn-default");
				$(this).addClass("btn-success");
			} else if ($(this).data('agree') == '0') {
				$(this).removeClass("btn-default");
				$(this).addClass("btn-warning");
			}
			let agree = $(this).data('agree');

			$.ajax({
				headers: {
					'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
				},
				type: "POST",
				url: appURL + "invite/save",
				data: {
					slug: slug,
					accept: agree
				},
				success: function (data) {
					console.log(data);
					loaderPage('hide');
				},
				error: function(XMLHttpRequest, textStatus, errorThrown) {
					console.log("XMLHttpRequest", XMLHttpRequest);
					console.log("textStatus", textStatus);
					console.log("errorThrown", errorThrown);
					loaderPage('hide');
				}
			});

			return false;
		});
	}
	
